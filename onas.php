	<h2 class="box_tag left_right_padding">Kim jesteśmy?</h2>
	<p>Kościół Ewangelicznych Chrześcijan wywodzi się z nurtu kościołów protestanckich powstałych w XVI wieku, w okresie Wielkiej Reformacji Kościoła Katolickiego.</p>

<p>Ruch Reformacyjny wyrósł na gruncie niezadowolenia z sytuacji istniejącej w ówczesnym Kościele. Duchowni i osoby świeckie w wyniku rozważania Pisma Świętego dostrzegli, że ówczesny Kościół oddalił się od nauki Jezusa Chrystusa i Apostołów. Intencją reformatorów było doświadczenie duchowego przebudzenia i dokonanie zwrotu od ludzkiej tradycji do nauki Pisma Świętego. Niezrozumiani i prześladowani ostatecznie zostali wyłączeni z Kościoła. W rezultacie tego powstało wiele wyznań protestanckich, z których większość przyjęła zasadę Reformacji: tylko  Łaska, tylko Pismo, tylko Wiara. Kościół Ewangelicznych Chrześcijan jest jednym z takich wyznań.</p>

<p>W XIX wieku wskutek przebudzenia duchowego ruch ewangelicznych chrześcijan rozwija się na terenie Rosji, Ukrainy, a później od 1908 roku także na ziemiach Polski.</p>

<p>Kościół Ewangelicznych Chrześcijan działając legalnie na mocy Ustawy o związkach wyznaniowych jest obecnie rozwijającą się mniejszość wyznaniową w naszym kraju.</p>

